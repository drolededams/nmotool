#include "libft.h"

int		ft_max(int *tab, size_t size)
{
	int		max;
	size_t	i;

	max = tab[0];
	i = 1;
	while (i < size)
	{
		if (tab[i] > max)
			max = tab[i];
		i++;
	}
	return (max);
}
