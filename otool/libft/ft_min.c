#include "libft.h"

int		ft_min(int *tab, size_t size)
{
	int		min;
	size_t	i;

	min = tab[0];
	i = 1;
	while (i < size)
	{
		if (tab[i] < min)
			min = tab[i];
		i++;
	}
	return (min);
}
